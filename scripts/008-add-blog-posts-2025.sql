## The Challenge of Heritage Surface Cleaning

Historic surfaces are vulnerable because soft stone (sandstone, limestone) erodes quickly under pressure, mortar joints (historic lime mortar) are weaker than modern cement, render finishes (historic lime render) are delicate and irreplaceable, slates can be fractured or split, and patina (the weathered appearance) is part of their character.

## Common Heritage Property Surfaces Across Purbeck

### Stone (Sandstone, Limestone, Purbeck Stone)

**Characteristics**: Relatively soft, porous, vulnerable to erosion
**Common Problems**: Algae, lichen, moss, dirt accumulation
**Cleaning Risks**: Pressure washing erodes surface; acid-based cleaners damage stone
**Safe Approach**: Gentle softwash with pH-neutral cleaners, allowing dwell time for organic growth removal without pressure

### Historic Render (Lime Mortar, Historic Plaster)

**Characteristics**: Weak and irreplaceable; traditional lime-based render is softer than modern cement
**Common Problems**: Algae, moss, dirt, weathering
**Cleaning Risks**: Pressure washing removes render; water-based cleaners can soften lime mortar
**Safe Approach**: Very gentle softwash or dry brushing; specialist knowledge essential

### Historic Slate Tiles

**Characteristics**: Brittle; can fracture or split if struck wrong
**Common Problems**: Lichen, moss, weathering
**Cleaning Risks**: High pressure fractures slate; walked-on slates crack
**Safe Approach**: Very gentle softwash or hand-brushing; no pressure washing

### Historic Brickwork

**Characteristics**: Varies greatly by age; older bricks much softer than modern
**Common Problems**: Algae, lichen, dirt, efflorescence (salt deposits)
**Cleaning Risks**: Pressure washing erodes mortar joints and soft brick surfaces
**Safe Approach**: Softwash with appropriate dwell times; sometimes dry-brushing safer than water-based approaches

## The Right Way to Clean Heritage Properties

### Assessment First

Before any cleaning, professional assessment determines material type, condition, historic significance, prior damage history, and appropriate method. Never assume all stone, render, or brick should be cleaned the same way.

### Appropriate Cleaning Methods

**For Soft Stone**: Soft wash with pH-neutral cleaners, organic-acid-based cleaners, hand-brushing for delicate areas. Absolutely NO pressure washing or caustic treatments.

**For Historic Render**: Very gentle softwash only, long dwell times, minimal water use, hand-brushing for final cleaning. NO pressure or aggressive scrubbing.

**For Historic Slate**: Dry-brushing when possible, very gentle softwash if water needed. NO pressure washing or roof walking.

**For Historic Brickwork**: Assessment of mortar type, soft wash appropriate to mortar type. NO pressure washing or acid cleaning.

### Working with Conservation Professionals

Properties with conservation constraints often require approval before cleaning, documentation of methods used, and specialist contractors experienced with heritage materials. We work closely with conservation bodies.

